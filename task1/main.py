a = ["ежевика","Иван","лента","скорострел","вампир","яйца"]
#Счётчик слов с буквой в
count_v = 0
#Проверка каждой строки из списка на наличие буквы в
for string in a:
        if "в" in string:
            count_v +=1
#Вывод количества слов с буквой в
print("Количество строк, содержащих букву в : ", count_v)